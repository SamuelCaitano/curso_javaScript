let num1 = 5.56663292 

console.log(`floor = ${Math.floor(num1)}`) // Retorna o maior inteiro
console.log(`ceil = ${Math.ceil(num1)}`) // Retorna o menor inteiro
console.log(`round = ${Math.round(num1)}`) // Retorna o valor do número xarredondado para o inteiro mais próximo.
console.log(`trunc = ${Math.trunc(num1)}`) // Retorna a parte inteira de x, removendo quaisquer dígitos fracionários.
console.log(`random = ${Math.round(Math.random() * (5 - 1) + 1)}`) // Retorna um número pseudoaleatório entre 1 e 5. 
console.log(`max = ${Math.max(1,2,3)}`)
console.log(`min = ${Math.min(1,2,3)}`)
console.log(`abs = ${Math.abs(num1)}`)
console.log(`pow = ${Math.pow(2,6)}`) // igual a (2 ** 6). Retorna a base x para a potência do expoente y(ou seja, x/y).