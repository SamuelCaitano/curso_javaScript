// Exemplo: pré-incremento
let num1 = 0

for(i = 0; i < 5; i++)
  console.log(++num1)

console.log('================')

// Exemplo: pós-incremento
let num2 = 0

for(i = 0; i < 5; i++)
  console.log(num2++)

console.log('================')

// Exemplo: pré-decremento
let num3 = 0

for(i = 0; i < 5; i++)
  console.log(--num3)

console.log('================')

// Exemplo: pós-decremento
let num4 = 0

for(i = 0; i < 5; i++)
  console.log(num4--)