/**
 * ============================
 *      PRECEDÊNCIA
 * ============================
 *   1  ()
 *   2  **
 *   3  * / %
 *   4  + -
 * ============================
 *  
 *  Adição => +
 *  Subtração => -
 *  Multiplicação => *
 *  Divisão => /
 *  Potênciação => **
 *  Modulo => %
 * 
 */

num1 = 5
num2 = 3

console.log( num1 + num2 )
console.log( num1 - num2 )
console.log( num1 / num2 )
console.log( num1 * num2 )
console.log( num1 ** num2 )
console.log( num1 % num2 )
console.log( num1 + num2 - 2 / num2 )